int	ft_atoi(char *str)
{
	int	i;
	int	nb;
	int	neg;

	neg = 0;
	nb = 0;
	i = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg += 1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		nb *= 10;
		nb += (str[i] - 48);
		i++;
	}
	if (neg % 2 == 1)
		return (-1 * nb);
	else
		return (nb);
}

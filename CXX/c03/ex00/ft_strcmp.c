int	ft_strcmp(char *s1, char *s2)
{
	int	compare;
	int	i;

	compare = 0;
	i = 0;
	while ((s1[i] != '\0') && (s2[i] != '\0') && (s1[i] == s2[i]))
		i++;
	if (s1[i] != s2[i])
		compare = (unsigned char) s1[i] - (unsigned char) s2[i];
	return (compare);
}
